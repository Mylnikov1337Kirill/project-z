export function applyMissionFeedback(mission: Mission): Mission {
  const retryPrinciple = mission.retryPrinciple ?? missionRetryPrinciplePatches[mission.id]

  if (mission.kind === 'boss-fight') {
    return {
      ...mission,
      retryPrinciple,
      rounds: mission.rounds.map(
        (round) => applyMissionFeedback(round) as BossFightRoundMission,
      ),
    }
  }

  const patch = missionFeedbackPatches[mission.id]
  const missionWithRetryPrinciple = retryPrinciple
    ? {
        ...mission,
        retryPrinciple,
      }
    : mission

  if (!patch) {
    return missionWithRetryPrinciple
  }

  if (missionWithRetryPrinciple.kind === 'scenario-decision' && patch.options) {
    return {
      ...missionWithRetryPrinciple,
      options: missionWithRetryPrinciple.options.map((option) => {
        const optionPatch = patch.options?.[option.id]

        return optionPatch
          ? {
              ...option,
              failureFeedback: option.failureFeedback ?? optionPatch.feedback,
              trapId: option.trapId ?? optionPatch.trapId,
              trapLabel: option.trapLabel ?? optionPatch.trapLabel,
            }
          : option
      }),
    }
  }

  if (missionWithRetryPrinciple.kind === 'chip-picker' && patch.chips) {
    return {
      ...missionWithRetryPrinciple,
      chips: missionWithRetryPrinciple.chips.map((chip) => {
        const chipPatch = patch.chips?.[chip.id]

        return chipPatch
          ? {
              ...chip,
              feedback: chip.feedback ?? chipPatch.feedback,
              trapId: chip.trapId ?? chipPatch.trapId,
              trapLabel: chip.trapLabel ?? chipPatch.trapLabel,
            }
          : chip
      }),
    }
  }

  if (missionWithRetryPrinciple.kind === 'chip-ordering' && patch.orderFeedback) {
    return {
      ...missionWithRetryPrinciple,
      orderFeedback: {
        ...patch.orderFeedback,
        ...missionWithRetryPrinciple.orderFeedback,
      },
    }
  }

  return missionWithRetryPrinciple
}

export function applyMissionFeedbackList(missions: Mission[]) {
  return missions.map(applyMissionFeedback)
}
